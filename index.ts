class Person{
    firstname: string;
    lastname:string; 
    age1: number;
    age2: number;
    Motto:string; 
    Picture:any;

    constructor(firstname:string, lastname: string,age1:number,age2:number,Motto:string,Picture:any) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.age1 = age1;
        this.age2= age2;
        this.Motto = Motto;
        this.Picture = Picture;
}
}